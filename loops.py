# Incrementing values
number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing values
a = 10
while a >= 1:
    print(a)
    a -= 1

# For loop
languages =["python", "PHP", "kotlin","C++"]
for x in languages:
    print(x)

# Range
for y in range(10):
    print(y)

for b in range(15, 20):
    print(b)
