#Arithmetic operators
x = 67
y = 3
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #modulus - returns the remainder after dividing two numbers

# comparison operator
num1 = 78
num2 = 45
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #equal to
print(num1 != num2) #not equal to

#  assignment operators
a = 7
print(a)

b = 13
b += 2
print(b)

c = 14
c -= 2
print(c)

# Logical operators
number1 = 8
number2 = 20
print(number1>number2 and number2>number1)
print(number1>number2 or number2>number1)
print(not (number1>number2 or number2>number1))